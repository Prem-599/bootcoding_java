public class OperatorEx1 {
    public static void main(String[] args) {
        //Example of operators
//Arithmetic Operator
// + -  * / %
        int a=10;
        int b=20;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
