public class StarPattern {
    public static void main(String[] args) {
        int n=5;
        int i ,j ;
        for ( i=1;i<=n;i++)// for row
        {
            for(j=1;j<=n;j++) //column
            {
                System.out.print(" P ");
            }
            System.out.println();
        }

    }

}
